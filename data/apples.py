"""The Orchard — source of truth for the 8-bit apple catalogue.

Every apple is defined here as an :class:`Apple`. ``build.py`` reads this list
and emits ``web/apples.json`` for the front end. Keep the data honest and the
flavor text fun.

Stats (sweetness / tartness / crispness) are on a 1-5 scale, RPG style.
"""

from __future__ import annotations

from dataclasses import dataclass, field, asdict


@dataclass(frozen=True)
class Origin:
    country: str
    region: str
    year: int  # approximate year the variety was discovered / developed


@dataclass(frozen=True)
class Apple:
    id: str                      # url-safe slug, unique
    name: str
    aka: list[str]               # synonyms / trade names
    origin: Origin
    parentage: str               # cross or "Chance seedling"
    skin: str                    # hex base colour, drives the pixel sprite
    blush: str | None            # optional hex stripe/blush overlay (bicolour skins)
    sweetness: int               # 1-5
    tartness: int                # 1-5
    crispness: int               # 1-5
    best_for: list[str]          # eating / baking / cider / sauce / salad / storage
    season: str                  # harvest window
    flavor_text: str             # RPG-style one-liner
    notes: str                   # a real fact worth knowing
    wiki: str | None = None      # optional direct Wikipedia URL; else the UI links a search

    def to_dict(self) -> dict:
        return asdict(self)


