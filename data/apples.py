def validate(apples: list[Apple]) -> None:
    """Cheap integrity checks so bad data fails loudly at build time."""
    seen: set[str] = set()
    for a in apples:
        if a.id in seen:
            raise ValueError(f"duplicate apple id: {a.id!r}")
        seen.add(a.id)
        for stat_name, stat in (("sweetness", a.sweetness), ("tartness", a.tartness), ("crispness", a.crispness)):
            if not 1 <= stat <= 5:
                raise ValueError(f"{a.id}: {stat_name} {stat} out of range 1-5")
        if not a.skin.startswith("#") or len(a.skin) != 7:
            raise ValueError(f"{a.id}: skin {a.skin!r} must be a #rrggbb hex colour")


